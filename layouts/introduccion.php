		<h1>Organización del G20</h1>

		<p>El G20 es el principal foro intergubernamental de coordinación económica y financiera internacional, con creciente gravitación en temas de naturaleza política para contribuir a la búsqueda de soluciones a los principales problemas de la agenda global.</p>
		
		<br>
		<p>Sus miembros representan casi el 90 por ciento del producto bruto global, dos tercios de la población mundial, el 80 por ciento del comercio internacional y más del 80 por ciento de las inversiones globales en investigación y desarrollo. (S.F, 2018)</p>
